fun main() {
    var x = 22
    println("Integer value: $x")

    var y = 1.5
    println("Float value: $y")

    var ch = 'T'
    println("Character value: $ch")

    var s = "HMS"
    println("String value: $s")

    var b = false
    println("Boolean value: $b")

    var d = 96.36
    println("Double value: $d")

    var l = 338927847
    println("Long value: $l")

    var sh = -2
    println("Short value: $sh")

    var a = 127
    println("Byte value: $a")
}